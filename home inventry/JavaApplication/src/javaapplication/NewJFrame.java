    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //getting values from user
        String user = jTextField1.getText();
        String pwd = jTextField2.getText();
        String url="jdbc:mysql://localhost:3306/javadb";
        try
       {     
            Connection myconn= DriverManager.getConnection(url, "root", "");           
           {
               //checking for empty values
               if(user.isEmpty() || pwd.isEmpty())
               JOptionPane.showMessageDialog(rootPane, "Please fill the all the values");
               
               //checking for validation of user
               else{
                   PreparedStatement ps = myconn.prepareStatement("select * from login");
                   ResultSet rs=ps.executeQuery();  
                   while (rs.next()) {
                   String name = rs.getString("user");
                   String pwds = rs.getString("password");
                   
                   //security checking
                   if(user.equals(name) && pwd.equals(pwds))
                   {
                                              this.hide();
                               
                   }
                   else{
                       JOptionPane.showMessageDialog(rootPane, "Account not EXIST");
                   }
            }//now on 1st row  
               }
            }
       }    
       catch(SQLException e)
            {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
       catch(Exception e)
            {
               JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }        
    }//GEN-LAST:event_jButton1ActionPerformed

